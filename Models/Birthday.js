const mongoose =
    require("mongoose");

module.exports =
    mongoose.model(

        "Birthday",

        new mongoose.Schema({

            //////////////////////////////////////////////////
            // SERVER
            //////////////////////////////////////////////////

            guildId: {

                type: String,

                required: true
            },

            //////////////////////////////////////////////////
            // USER
            //////////////////////////////////////////////////

            userId: {

                type: String,

                required: true
            },

            //////////////////////////////////////////////////
            // DAY
            //////////////////////////////////////////////////

            day: {

                type: Number,

                required: true
            },

            //////////////////////////////////////////////////
            // MONTH
            //////////////////////////////////////////////////

            month: {

                type: Number,

                required: true
            },

            //////////////////////////////////////////////////
            // CHANNEL
            //////////////////////////////////////////////////

            channelId: {

                type: String,

                default: null
            },

            //////////////////////////////////////////////////
            // CREATED
            //////////////////////////////////////////////////

            createdAt: {

                type: Date,

                default: Date.now
            }
        })
    );