module.exports = async (userData) => {

    //////////////////////////////////////////////////
    // SIN DEUDA
    //////////////////////////////////////////////////

    if (

        !userData.loanTaken ||

        userData.debt <= 0 ||

        !userData.loanDate

    ) return 0;

    //////////////////////////////////////////////////
    // FECHAS
    //////////////////////////////////////////////////

    const now =
        new Date();

    //////////////////////////////////////////////////

    const loanDate =
        new Date(
            userData.loanDate
        );

    //////////////////////////////////////////////////
    // DIFERENCIA
    //////////////////////////////////////////////////

    const diff =
        now - loanDate;

    //////////////////////////////////////////////////
    // DIAS
    //////////////////////////////////////////////////

    const daysPassed =
        Math.floor(

            diff /

            (1000 * 60 * 60 * 24)
        );

    //////////////////////////////////////////////////
    // SI NO PASÓ UN DIA
    //////////////////////////////////////////////////

    if (daysPassed <= 0)
        return 0;

    //////////////////////////////////////////////////
    // INTERES
    //////////////////////////////////////////////////

    const interestPerDay =
        0.05;

    //////////////////////////////////////////////////
    // DEUDA ORIGINAL
    //////////////////////////////////////////////////

    const originalDebt =
        userData.debt;

    //////////////////////////////////////////////////
    // NUEVA DEUDA
    //////////////////////////////////////////////////

    let newDebt =
        originalDebt;

    //////////////////////////////////////////////////
    // INTERES COMPUESTO
    //////////////////////////////////////////////////

    for (

        let i = 0;

        i < daysPassed;

        i++

    ) {

        newDebt +=
            Math.floor(

                newDebt *
                interestPerDay
            );
    }

    //////////////////////////////////////////////////
    // LIMITE MAXIMO
    //////////////////////////////////////////////////

    const maxDebt =

        originalDebt * 3;

    //////////////////////////////////////////////////

    if (
        newDebt > maxDebt
    ) {

        newDebt =
            maxDebt;
    }

    //////////////////////////////////////////////////
    // DIFERENCIA
    //////////////////////////////////////////////////

    const added =
        newDebt - originalDebt;

    //////////////////////////////////////////////////
    // UPDATE
    //////////////////////////////////////////////////

    userData.debt =
        newDebt;

    //////////////////////////////////////////////////

    userData.loanDate =
        now;

    //////////////////////////////////////////////////

    await userData.save();

    //////////////////////////////////////////////////

    return added;
};