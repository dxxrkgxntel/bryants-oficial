module.exports = async (userData) => {

    //////////////////////////////////////////////////
    // SIN BANCO
    //////////////////////////////////////////////////

    if (
        userData.bank <= 0
    ) return 0;

    //////////////////////////////////////////////////
    // TIME
    //////////////////////////////////////////////////

    const now = Date.now();

    //////////////////////////////////////////////////

    if (
        !userData.lastBankBonus
    ) {

        userData.lastBankBonus = now;

        return 0;
    }

    //////////////////////////////////////////////////
    // 24 HORAS
    //////////////////////////////////////////////////

    const cooldown =

        24 * 60 * 60 * 1000;

    //////////////////////////////////////////////////

    if (

        now - userData.lastBankBonus
        < cooldown

    ) {

        return 0;
    }

    //////////////////////////////////////////////////
    // BONUS
    //////////////////////////////////////////////////

    const bonus = Math.floor(

        userData.bank * 0.01
    );

    //////////////////////////////////////////////////

    if (
        bonus <= 0
    ) return 0;

    //////////////////////////////////////////////////
    // DAR BONUS
    //////////////////////////////////////////////////

    userData.bank += bonus;

    //////////////////////////////////////////////////

    userData.lastBankBonus = now;

    //////////////////////////////////////////////////

    return bonus;
};